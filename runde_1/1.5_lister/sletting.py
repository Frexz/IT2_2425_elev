### Sletting av lister

# Med del
venner = ['Ole', 'Dole', 'Doffen', 'Donald']
del venner # Sletter hele listen med variabel sÃ¥ den ikke eksisterer lengre

# Med .clear()
venner = ['Ole', 'Dole', 'Doffen', 'Donald']
venner.clear()
print(venner)

# Med tom liste
venner = ['Ole', 'Dole', 'Doffen', 'Donald']
venner = []
print(venner)
